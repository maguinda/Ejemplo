void listar(){
    String sql ="select * from persona";
    try{
        cn=con.getConnection();
        st=cn.createStatement();
        rs=st.executeQuery(sql);
        Object[]persona=new Object[3];
        modelo = (DefaultTableModel)TablaDatos.getModel();
        while(rs.next()){
            persona[0]=rs.getInt("Id");
            persona[1]=rs.getString("DNI");
            persona[2]=rs.getString("Nombres");
            modelo.addRow(persona);
        }
        TablaDatos.setModel(modelo);
    }catch(Exception e){
    }
}

void Agregar(){
    String dni= txtDni.getText();
    String nom = txtNombre.getText();
    if (dni.equals("") || nom.equals("")){
        JOptionPane.showMessageDialog(null, "Cajas vacias...!!!");
    }else{
        String sql= "insert into persona(DNI, Nombres)values(' "+dni+" ',' "+nom+" ')";
    
    try{
        cn=con.getConnection();
        st=cn.createStatement();
        st.executeUpdate(sql);
        JOptionPane.showMessageDialog(null, "Usuario Agregado");
        limpiartabla();
    }catch(Exception e){
    }
}

}

void modificar(){
    //antes recibimos los valores que vamos a modificar en : nom y dni
    String nom= txtNombre.getText();
    String dni= txtDni.getText();
    
    String sql= "Update persona set DNI=' "+dni+" ',Nombres=' "+nom+" ' where id="+id;
    if(dni.equals("")||nom.equals("")){
        JOptionPane.showMessageDialog(null, "Debe ingresar dato");
    }else{
        try{
            cn=con.getConnection();
            st=cn.createStatement();
            st.execute(sql);
            JOptionPane.showMessageDialog(null, "Usuario actualizado");
            limpiartabla();
        }catch(Exception e){
            
        }
    }
}
void limpiartabla(){
    for(int i=0;i<=TablaDatos.getRowCount();i++){
    modelo.removeRow(i);
    i=i-1;
}
}

void eliminar(){
    int filaseleccionada = TablaDatos.getSelectedRow();
    if(filaseleccionada==-1){
        JOptionPane.showMessageDialog(null, "Debe seleccionar fila");
    }else{
        String sql="delete from persona where Id=" + id;
        try{
            cn=con.getConnection();
            st=cn.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Usuario Eliminado con exito....");
            limpiartabla();
        }catch(Exception e){
        }
    }
}

