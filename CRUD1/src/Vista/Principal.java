    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        // TODO add your handling code here:
        int fila=TablaDatos.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "Usuario no seleccionado");
        }else{
            id=Integer.parseInt((String) TablaDatos.getValueAt(fila,0).toString());//fila columna 0
            String dni=(String)TablaDatos.getValueAt(fila, 1);//fila columna 1
            String nom=(String)TablaDatos.getValueAt(fila, 2);//fila columna 2
            txtId.setText(""+id);//coloca valores en la caja respectivamente.
            txtDni.setText(dni);
            txtNombre.setText(nom);
        }
    }//GEN-LAST:event_TablaDatosMouseClicked

